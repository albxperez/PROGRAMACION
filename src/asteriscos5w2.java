import java.util.Scanner;

public class asteriscos5w2 {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        System.out.println("Introduce un numero: ");
        int num= sc.nextInt();

        for (int i=0 ; i<num ; i++) {
            System.out.print("");
        }

        for (int j=0 ; j< num - j; j++ ) {
            System.out.println("*");
        }
        System.out.println("");
        }

    }

