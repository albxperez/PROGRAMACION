public class asteriscos6 {
    public static void main(String[] args) {
        System.out.print("  ");//centrar la punta de la pirámide
        System.out.println("*");//punta de la pirámide
        for(int col=0;col<=0;col++){
            System.out.print(" ");//centrar la segunda fila de la pirámide
            for(int n=2;n<=4;n++){//segunda fila de la pirámide
                System.out.print("*");
            }

            System.out.println();//salto de linea
            for( col=0;col<=0;col++) {
                for (int n = 0;n <= 4; n++) {//última fila de la pirámide
                    System.out.print("*");
                }
                    }
                }
            }
}